<h3>4. Объем и категории обрабатываемых персональных данных, категории
субъектов персональных данных
</h3>
<p>4.1. Объем и содержание обрабатываемых персональных данных должны
соответствовать принципам и целям обработки, предусмотренным настоящей Политикой.
</p>
<p>4.2. Оператор обрабатывает персональные данные следующих категорий субъектов
персональных данных:
</p>
<p>• персональные данные работников Оператора, бывших работников, кандидатов на
замещение вакантных должностей, а также родственников работников – информация,
необходимая Оператору в связи с трудовыми отношениями;
</p>
<p>• персональные данные клиентов, партнеров и контрагентов Оператора (физических
лиц), а также работников/представителей юридического лица, являющегося клиентом
или контрагентом Оператора – информация, необходимая Оператору для выполнения
своих обязательств в рамках договорных отношений;
</p>
<p>• персональные данные иных лиц – информация, необходимая Оператору для
реализации своих прав и законных интересов в рамках осуществления видов
.<p>
деятельности, предусмотренных Уставом и иными локальными нормативными актами.
<p>4.3. Оператор обрабатывает следующие категории персональных данных:
</p>
<p>- пол;
</p>
