 <h2>Политика компании в отношении обработки персональных данных
 <small>(далее — Политика)
 </small></h2>
<h3>1. Общие положения
</h3>
<p>1.1. Настоящая Политика разработана во исполнение требований Конституции
Российской Федерации, п. 2 ч. 1 и ч. 2 ст. 18.1 Федерального закона от 27.07.2006 N 152-ФЗ
"О персональных данных» (далее — Закон о персональных данных), Трудового кодекса
Российской Федерации, Федерального закона от 27.07.2006 г. № 149-ФЗ "Об информации,
информационных технологиях и о защите информации" и с учетом положений действующего
законодательства.</p>
<p>Настоящая Политика определяет основные принципы, цели, условия и способы
обработки персональных данных, перечни субъектов обработки персональных данных и
обрабатываемых Оператором персональных данных, права и обязанности Оператора при
обработке персональных данных, права субъектов персональных данных.
</p>
<p>1.2. Настоящая Политика разработана в целях обеспечения защиты прав и свобод
человека и гражданина при обработке его персональных данных, в том числе защиты прав на
неприкосновенность частной жизни, личную и семейную тайну.
</p>
<p>1.3. Настоящая Политика действует в отношении всех обрабатываемых персональных
данных, полученных как до издания соответствующей Политики, так и после, за исключением
случаев, когда положения настоящей Политики не могут быть распространены на отношения
по обработке и защите персональных данных, полученных до ее утверждения.
</p>
<p>1.4. Основные понятия, используемые в настоящей Политике:
</p>
<p>• персональные данные - любая информация, относящаяся к прямо или косвенно
определенному или определяемому физическому лицу (субъекту персональных
данных);
</p>
<p>• оператор самостоятельно или совместно с другими лицами организующие и (или)
осуществляющие обработку персональных данных, а также определяющие цели
обработки персональных данных, состав персональных данных, подлежащих
обработке, действия (операции), совершаемые с персональными данными;
</p>
<p>• обработка персональных данных - любое действие (операция) или совокупность
действий (операций), совершаемых с использованием средств автоматизации или без
использования таких средств с персональными данными, включая сбор, запись,
систематизацию, накопление, хранение, уточнение (обновление, изменение),
извлечение, использование, передачу (распространение, предоставление, доступ),
обезличивание, блокирование, удаление, уничтожение персональных данных;
</p>
<p>• автоматизированная обработка персональных данных - обработка персональных
данных с помощью средств вычислительной техники;
</p>
<p>• распространение персональных данных - действия, направленные на раскрытие
персональных данных неопределенному кругу лиц;
</p>
<p>• предоставление персональных данных - действия, направленные на раскрытие
персональных данных определенному лицу или определенному кругу лиц;
</p>
<p>• блокирование персональных данных - временное прекращение обработки
персональных данных (за исключением случаев, если обработка необходима для
уточнения персональных данных);
</p>
<p>• уничтожение персональных данных - действия, в результате которых становится
невозможным восстановить содержание персональных данных в информационной
системе персональных данных и (или) в результате которых уничтожаются
материальные носители персональных данных;
</p>
<p>• обезличивание персональных данных - действия, в результате которых становится
невозможным без использования дополнительной информации определить
принадлежность персональных данных конкретному субъекту персональных данных;
</p>
<p>• информационная система персональных данных - совокупность содержащихся в базах
данных персональных данных и обеспечивающих их обработку информационных
технологий и технических средств;
</p>
<p>• трансграничная передача персональных данных - передача персональных данных на
территорию иностранного государства органу власти иностранного государства,
иностранному физическому лицу или иностранному юридическому лицу.
</p>
<p>1.5. Оператор осуществляет сбор, запись, систематизацию, накопление, хранение,
уточнение (обновление, изменение), извлечение, использование, передачу (распространение,
предоставление, доступ), обезличивание, блокирование, удаление, уничтожение
персональных данных.
</p>
<p>1.6. Настоящая политика является общедоступным документом и публикуется в сети
Интернет на сайте Оператора.
</p>
<h3>2. Основные принципы, цели и случаи обработки персональных данных
</h3>
<p>2.1. Обработка персональных данных субъектов персональных данных осуществляется
Оператором на основе следующих принципов:
</p>
<p>• законности обработки персональных данных;
</p>
<p>• обработка персональных данных должна ограничиваться достижением конкретных,
заранее определенных и законных целей
;<p>
<p>• не допускается обработка персональных данных, несовместимая с целями сбора
персональных данных;
</p>
<p>• не допускается объединение баз данных, содержащих персональные данные, обработка
которых осуществляется в целях, несовместимых между собой;
</p>
<p>• не допускается избыточность обрабатываемых персональных данных по отношению к
заявленным целям их обработки;
</p>
<p>• при обработке персональных данных обеспечивается точность персональных данных,
их достаточность, а в необходимых случаях и актуальность по отношению к
заявленным целям их обработки;
</p>
<p>• хранение персональных данных осуществляется в форме, позволяющей определить
субъекта персональных данных, не дольше, чем этого требуют цели обработки
персональных данных, если срок хранения персональных данных не установлен
федеральным законом либо договором, стороной, выгодоприобретателем или
поручителем по которому является субъект персональных данных;
</p>
<p>• право доступа для обработки персональных данных имеют работники Оператора в
соответствии с возложенными на них должностными обязанностями;
</p>
<p>• обрабатываемые персональные данные уничтожаются либо обезличиваются по
достижении целей обработки или в случае утраты необходимости в достижении этих
целей, если иное не предусмотрено федеральным законом.
</p>
<p>2.2. Целями обработки персональных данных являются:
</p>
<p>• регулирование трудовых и иных связанных с ними отношений с работниками
Оператора;
</p>
<p>• подготовка, заключение, исполнение и прекращение гражданско-правовых договоров
с контрагентами;
</p>
<p>• реализация прав и законных интересов Оператора в рамках осуществления видов
деятельности, предусмотренных Уставом и иными локальными нормативными актами
Оператора либо достижения общественно значимых целей;
</p>
<p>• обеспечение соблюдения требований законодательства Российской Федерации;
</p>
<p>• исполнения обязанностей, возложенных на Оператора законодательством Российской
Федерации, в том числе связанных с представлением персональных данных в
налоговые органы, Пенсионный фонд Российской Федерации, Фонд социального
страхования Российской Федерации, Федеральный фонд обязательного медицинского
страхования, а также в иные государственные органы;
</p>
<p>• соблюдение конфиденциальности информации при обработке персональных данных;
</p>
<p>• осуществление пропускного режима;
</p>
<p>• ведение бухгалтерского учета и иной отчетности Оператора;
</p>
<p>• продвижения товаров, работ, услуг на рынке путем осуществления прямых контактов
с потенциальным потребителем с помощью средств связи;
</p>
<p>2.3. Обработка персональных данных Оператором допускается, если она:
