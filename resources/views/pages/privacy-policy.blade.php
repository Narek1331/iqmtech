</p>
<p>• получена от третьего лица с последующим незамедлительным уведомлением и
получением согласия субъекта персональных данных
;</p>
<p>• необходима для исполнения договора/контракта, в котором субъект персональных
данных выступает стороной, выгодоприобретателем или поручителем;
</p>
<p>• необходима для защиты жизни, здоровья или иных жизненно важных интересов
субъекта персональных данных, если получение согласия субъекта персональных
данных невозможно;
</p>
<p>• необходима для осуществления прав и законных интересов Оператора при условии, что
при этом не нарушаются права и свободы субъекта персональных данных;
</p>
<p>• необходима для достижения целей, предусмотренных международным договором
Российской Федерации или законом, для осуществления и выполнения возложенных
законодательством Российской Федерации на оператора функций, полномочий и
обязанностей;
</p>
<p>• осуществляется в связи с участием лица в конституционном, гражданском,
административном, уголовном судопроизводстве, судопроизводстве в арбитражных
судах;
</p>
<p>• необходима для исполнения судебного акта, акта другого органа или должностного
лица, подлежащих исполнению в соответствии с законодательством Российской
Федерации об исполнительном производстве (далее - исполнение судебного акта);
<p>• направлена на продвижение товаров, работ и услуг.
</p>
<p>2.4. Не допускается обработка персональных данных, которая не соответствует
законным целям.
</p>
