<h3>5. Права и обязанности Оператора
</h3>
<p>5.1. Права Оператора:

</p>
<p>• в установленном законом порядке поручить обработку третьим лицам при условии
предварительного получения согласия субъекта персональных данных, если иное не
установлено законом;

</p>
<p>• в установленном законом порядке получать персональные данные от третьих лиц при
условии уведомления Оператором субъекта персональных данных о начале
осуществления обработки его персональных данных и/или получения согласия на
обработку персональных данных от субъекта персональных данных;

</p>
<p>• принимать локальные нормативные акты, направленные на совершенствование
настоящей Политики;

</p>
<p>• отказывать в предоставлении персональных данных в случаях, предусмотренных
частью 6 статьи 14 и частью 2 статьи 20 Федерального закона «О персональных
данных»;

</p>
<p>• самостоятельно определять состав и перечень мер, необходимых и достаточных для
обеспечения выполнения обязанностей, предусмотренных Законом о персональных
данных и принятыми в соответствии с ним нормативными правовыми актами, если
иное не предусмотрено Законом о персональных данных или другими федеральными
законами;

</p>
<p>• привлекать к ответственности за нарушение законодательства о персональных данных
лиц, ответственных в соответствии с локальными нормативными актами за обработку
персональных данных;

</p>
<p>• осуществлять иные права, предоставленные Оператору в соответствии с
законодательством.

</p>
<p>5.2. Обязанности Оператора:

</p>
<p>• соблюдать законодательство Российской Федерации в области обработки
персональных данных;

</p>
<p>• предоставлять субъекту персональных данных запрашиваемую информацию в
соответствии с пунктами настоящей Политики;

</p>
<p>• разъяснить субъекту персональных данных юридические последствия отказа
предоставить его персональные данные, если предоставление персональных данных
является обязательным в соответствии с законодательством Российской Федерации;

</p>
<p>• предоставить субъекту персональных данных информацию, предусмотренную частью
3 статьи 18 Закона о персональных данных, если персональные данные получены не от
субъекта персональных данных до начала обработки таких персональных данных, за
исключением случаев, предусмотренных частью 4 этой же статьи;

</p>
<p>• обеспечить запись, систематизацию, накопление, хранение, уточнение (обновление,
изменение), извлечение персональных данных граждан Российской Федерации с
использованием баз данных, находящихся на территории Российской Федерации, при
сборе персональных данных, в том числе посредством информационнотелекоммуникационной сети "Интернет", за исключением случаев, указанных в
пунктах 2, 3, 4, 8 части 1 статьи 6 Закона о персональных данных;

</p>
<p>• не раскрывать третьим лицам и не распространять персональные данные без согласия
субъектов персональных данных;

</p>
<p>• информировать сотрудников о целях, источниках и способах получения персональных
данных, о характере этих персональных данных и последствиях отказа сотрудника дать
письменное согласие на их получение;

</p>
<p>• уведомить уполномоченный орган по защите прав субъектов персональных данных о
своем намерении осуществлять обработку персональных данных, за исключением
случаев, предусмотренных частью 2 статьи 22 Закона о персональных данных;

</p>
<p>• принимать меры, необходимые и достаточные для обеспечения выполнения
обязанностей, предусмотренных Законом о персональных данных и принятыми в
соответствии с ним нормативными правовыми актами;

</p>
<p>• устранять нарушения законодательства в области персональных данных, допущенные
при обработке, в том числе уточнять, блокировать и уничтожать персональные данные;

</p>
<p>• предоставлять по запросу уполномоченного органа по защите прав субъекта
персональных данных необходимую информацию в течение 30 (Тридцати) дней с даты
получения такого запроса;

</p>
<p>• разъяснить субъекту персональных данных порядок принятия решения на основании
исключительно автоматизированной обработки его персональных данных и
возможные юридические последствия такого решения, предоставить возможность
заявить возражение против такого решения, а также разъяснить порядок защиты
субъектом персональных данных своих прав и законных интересов.

</p>
<p>5.3. В случае, если Оператор осуществляет обработку персональных данных по
поручению другого оператора, то Оператор не обязан получать согласие субъекта
персональных данных на обработку его персональных данных.
</p>
<h3>6. Права и обязанности субъектов персональных данных
</h3>

<p>6.1. Субъект персональных данных вправе:

</p>
<p>6.1.1. Получать информацию по предварительному запросу, касающуюся обработки его
персональных данных, в том числе содержащей:

</p>
<p>• подтверждение факта обработки персональных данных оператором;

</p>
<p>• правовые основания и цели обработки персональных данных;

</p>
<p>• цели и применяемые оператором способы обработки персональных данных;

</p>
<p>• наименование и место нахождения оператора, сведения о лицах (за исключением
работников оператора), которые имеют доступ к персональным данным или которым
могут быть раскрыты персональные данные на основании договора с оператором или
на основании федерального закона;

</p>
<p>• обрабатываемые персональные данные, относящиеся к соответствующему субъекту
персональных данных, источник их получения, если иной порядок представления таких
данных не предусмотрен федеральным законом;

</p>
<p>• сроки обработки персональных данных, в том числе сроки их хранения;
<p>• порядок осуществления субъектом персональных данных прав, предусмотренных
настоящим Федеральным законом;

</p>
<p>• информацию об осуществленной или о предполагаемой трансграничной передаче
данных;

</p>
<p>• наименование или фамилию, имя, отчество и адрес лица, осуществляющего обработку
персональных данных по поручению оператора, если обработка поручена или будет
поручена такому лицу;

</p>
<p>• иные сведения, предусмотренные Законом о персональных данных или другими
федеральными законами.

</p>
<p>6.1.2. Обжаловать действия или бездействие оператора в уполномоченный орган по
защите прав субъектов персональных данных или в судебном порядке.

</p>
<p>6.1.3. Требовать от Оператора уточнения его персональных данных, их блокирования
или уничтожения в случае, если персональные данные являются неполными, устаревшими,
неточными, незаконно полученными или не являются необходимыми для заявленной цели
обработки, а также принимать предусмотренные законом меры по защите своих прав.
<p>6.1.4. Выдвигать условие предварительного согласия при обработке персональных
данных в целях продвижения на рынке товаров, работ и услуг.

</p>
<p>6.1.5. Отозвать согласие на обработку персональных данных.

</p>
<p>6.2. Субъект персональных данных обязан:

</p>
<p>6.2.1. Сообщать достоверную информацию, которая необходима для обработки
персональных данных Оператором.

</p>
<p>6.2.2. Предоставлять документы, содержащие персональные данные, в объеме,
необходимом для цели обработки.

</p>
<p>6.2.3. Сообщать Оператору об уточнении (обновлении, изменении) своих персональных
данных.
</p>
<h3>7. Порядок обработки персональных данных
</h3>.

<p>7.1. Обработка персональных данных осуществляется в соответствии с
законодательством Российской Федерации.

</p>
<p>7.2. Оператор осуществляет автоматизированную, смешанную и не автоматизированную
обработку персональных данных.

</p>
<p>7.3. Обработка персональных данных осуществляется путем:

</p>
<p>• получения персональных данных в устной и письменной форме непосредственно от
субъектов персональных данных;

</p>
<p>• получения персональных данных от третьих лиц при условии соблюдения порядка
получения согласия на обработку персональных данных, установленного
законодательством Российской Федерации;

</p>
<p>• получения персональных данных из общедоступных источников;

</p>
<p>• внесения персональных данных в журналы, реестры и информационные системы
Оператора;

</p>
<p>• использования иных способов обработки персональных данных.

</p>
<p>7.4. Оператор принимает необходимые правовые, организационные и технические меры
для защиты персональных данных от неправомерного или случайного доступа к ним,
уничтожения, изменения, блокирования, распространения и других несанкционированных
действий, в том числе:

</p>
<p>• определяет угрозы безопасности персональных данных при их обработке;

</p>
<p>• принимает необходимые документы, регулирующие отношения в сфере обработки и
защиты персональных данных;

</p>
<p>• принимает локальные акты по вопросам обработки персональных данных, а также
локальные акты, устанавливающие процедуры, направленные на предотвращение и
выявление нарушений законодательства Российской Федерации, устранение
последствий таких нарушений;

</p>
<p>• назначает лиц, ответственных за обработку персональных данных;
</p>
<p>• обучает своих работников правилам обработки персональных данных;
</p>
<p>• знакомит работников, задействованных в обработке персональных данных, под
роспись с настоящей Политикой и другими документами, регламентирующими
обработку персональных данных;
</p>
<p>• создает необходимые условия для работы с персональными данными;
</p>
<p>• контролирует и организует учет и регистрацию документов, содержащих
персональные данные;
</p>
<p>• контролирует процессы и организует работу информационной системы,
осуществляющей обработку персональных данных;
</p>
<p>• осуществляет учет машинных носителей персональных данных;
</p>
<p>• принимает меры по восстановлению персональных данных, модифицированных или
уничтоженных вследствие несанкционированного доступа к ним;
</p>
<p>• обеспечивает сохранность персональных данных и принимает иные необходимые в
соответствии с законодательством Российской Федерации организационные и
технические меры при их обработке;
</p>
<p>• проводит оценку вреда, нанесенного вследствие нарушения законодательства
Российской Федерации в области персональных данных в соответствии со ст. ст. 15,
151, 152, 1101 Гражданского кодекса Российской Федерации.
</p>
<p>7.5. К обработке персональных данных допускаются только те работники Оператора, в
должностные обязанности которых входят действия по обработке персональных данных.
Совершение указанных действий осуществляется только в объеме необходимом для
выполнения своих должностных обязанностей.
</p>
<p>Доступ к персональным данным также имеют генеральный директор, заместитель
генерального директора, начальники подразделений и бухгалтерия в рамках, необходимых для
осуществления ими конкретных функций.
</p>
<p>7.6. При сборе персональных данных, в том числе посредством</p> информационнотелекоммуникационной сети Интернет, Оператор обеспечивает запись, систематизацию,
накопление, хранение, уточнение (обновление, изменение), извлечение персональных данных
граждан Российской Федерации с использованием баз данных, находящихся на территории
Российской Федерации.</p>
<p>7.7. В случае передачи Оператором персональных данных третьему лицу,
осуществляющему обработку персональных данных по поручению Оператора или на
основании договора, должны соблюдаться следующие условия:
</p>
<p>• договор должен заключаться в письменной форме, существенным условием которого
является обязанность обеспечения третьим лицом условий конфиденциальности и
обеспечения безопасности персональных данных при их передаче или обработке;
</p>
<p>• третье лицо должно быть ознакомлено с настоящей Политикой и иными документами,
определяющими принципы, правила и условия обработки персональных данных;
</p>
<p>• должен быть определен срок обработки персональных данных;
</p>
<p>• передача персональных данных другому лицу должна соответствовать целям
обработки.
</p>
<p>7.8. Трансграничная передача персональных данных на территории иностранных
государств, являющихся сторонами Конвенции о защите физических лиц при
автоматизированной обработке персональных данных, а также иных иностранных государств,
обеспечивающих адекватную защиту прав субъектов персональных данных, осуществляется
в соответствии с Федеральным законом "О персональных данных" и может быть запрещена
или ограничена в целях защиты основ конституционного строя Российской Федерации,
нравственности, здоровья, прав и законных интересов граждан, обеспечения обороны страны
и безопасности государства. Трансграничная передача персональных данных на территорию
иностранного государства, не являющегося стороной указанной Конвенции, осуществляется
в соответствии с законодательными актами Российской Федерации при условии соответствия
действующих в этом государстве норм права и применяемых мер безопасности персональных
данных положениям Конвенции.
</p>
<p>7.9. Хранение персональных данных осуществляется в течение срока, необходимого для
достижения целей обработки и в форме, позволяющей с полной степенью достоверности
определить субъекта персональных данных, за исключением обработки персональных данных
способом обезличивания.
</p>
<p>7.10. По достижении целей обработки персональных данных, истечении сроков хранения
или отзыва согласия субъекта персональных данных на осуществление обработки
персональные данные подлежат уничтожению, если иное не предусмотрено
законодательством Российской Федерации или договором.
</p>
<h3>8. Порядок взаимодействия с субъектом персональных данных
</h3>
<p>8.1. Информация, указанная в п. 6.1.1 настоящей Политики, предоставляется субъекту
персональных данных на основании запроса в письменном виде, содержащего:
</p>
<p>1) номер основного документа, удостоверяющего личность субъекта персональных
данных или его представителя, сведения о дате выдачи указанного документа и выдавшем его
органе;
</p>
<p>2) сведения, подтверждающие участие субъекта персональных данных в отношениях с
оператором (номер договора, дата заключения договора, условное словесное обозначение и
(или) иные сведения);
</p>
<p>3) сведения, иным образом подтверждающие факт обработки персональных данных
оператором;
</p>
<p>4) подпись субъекта персональных данных или его представителя.
</p>
<p>8.2. Субъект персональных данных, которому по запросу была предоставлена указанная
выше информация имеет право обратиться с повторным запросом не ранее, чем через 30
(Тридцать) дней с момента первоначального запроса, если более короткий срок не установлен
федеральным законом, принятым в соответствии с ним нормативным правовым актом или
договором, стороной которого либо выгодоприобретателем или поручителем по которому
является субъект персональных данных.
</p>
<p>Субъект персональных данных вправе обратиться с повторным запросом (с
обоснованием его направления) до истечения указанного выше срока в случае предоставления
ему запрашиваемых данных об обработке персональных данных не в полном объеме.
</p>
<p>8.3. Оператор вправе мотивированно отказать субъекту персональных данных в
выполнении повторного запроса, не соответствующего указанным выше условиям.
</p>
<p>8.4. Субъект персональных данных вправе требовать от Оператора уточнения его
персональных данных, их блокирования или уничтожения в случае, если персональные
данные являются неполными, устаревшими, неточными, незаконно полученными или не
являются необходимыми для заявленной цели обработки, а также принимать
предусмотренные законом меры по защите своих прав.
</p>
<p>8.5. В срок, не превышающий 7 (Семь) рабочих дней со дня предоставления субъектом
персональных данных или его представителем сведений, подтверждающих, что персональные
